def add_p(doc, text, style="Normal", bold=False, italic=False):
    p = doc.add_paragraph(style=style)
    r = p.add_run(text)
    set_run_font(r, 14, bold=bold, italic=italic)
    return p


def add_title(doc, text, break_before=True):
    if break_before and len(doc.paragraphs) > 0:
        doc.add_page_break()
    p = doc.add_paragraph(style="SectionTitle")
    r = p.add_run(text.upper())
    set_run_font(r, 15, bold=True)
    return p


def add_sub(doc, text):
    p = doc.add_paragraph(style="Subsection")
    r = p.add_run(text)
    set_run_font(r, 14, bold=True)
    return p


def add_subsub(doc, text):
    p = doc.add_paragraph(style="Subsubsection")
    r = p.add_run(text)
    set_run_font(r, 14, bold=True, italic=True)
    return p


def set_cell_text(cell, text, bold=False):
    cell.vertical_alignment = WD_CELL_VERTICAL_ALIGNMENT.CENTER
    cell.text = ""
    p = cell.paragraphs[0]
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER if bold else WD_ALIGN_PARAGRAPH.LEFT
    p.paragraph_format.first_line_indent = Cm(0)
    p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.EXACTLY
    p.paragraph_format.line_spacing = Pt(14)
    r = p.add_run(str(text))
    set_run_font(r, 12, bold=bold)


def add_table(doc, caption, rows):
    p = doc.add_paragraph(style="CaptionTable")
    r = p.add_run(caption)
    set_run_font(r, 12)
    table = doc.add_table(rows=1, cols=len(rows[0]))
    table.style = "Table Grid"
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr = table.rows[0].cells
    for i, text in enumerate(rows[0]):
        set_cell_text(hdr[i], text, True)
    for row in rows[1:]:
        cells = table.add_row().cells
        for i, text in enumerate(row):
            set_cell_text(cells[i], text)
    doc.add_paragraph()
    return table


def add_figure(doc, intro, image_name, caption, width_cm=15.5):
    p = add_p(doc, intro)
    p.paragraph_format.keep_with_next = True
    p_img = doc.add_paragraph(style="BodyNoIndent")
    p_img.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p_img.paragraph_format.keep_with_next = True
    p_img.paragraph_format.line_spacing_rule = WD_LINE_SPACING.SINGLE
    p_img.paragraph_format.line_spacing = 1
    p_img.paragraph_format.space_before = Pt(6)
    p_img.paragraph_format.space_after = Pt(3)
    run = p_img.add_run()
    run.add_picture(str(ASSETS / image_name), width=Cm(width_cm))
    cap = doc.add_paragraph(style="CaptionFigure")
    r = cap.add_run(caption)
    set_run_font(r, 13)
    return cap


def add_toc(doc, toc_pages):
    p = doc.add_paragraph(style="SectionTitle")
    r = p.add_run("СОДЕРЖАНИЕ")
    set_run_font(r, 15, bold=True)
    for title, level in TOC_ITEMS:
        p = doc.add_paragraph(style="BodyNoIndent")
        p.paragraph_format.left_indent = Cm(0.75 * level)
        p.paragraph_format.first_line_indent = Cm(0)
        p.paragraph_format.line_spacing_rule = WD_LINE_SPACING.EXACTLY
        p.paragraph_format.line_spacing = Pt(18)
        p.paragraph_format.tab_stops.add_tab_stop(Cm(17 - 0.75 * level), WD_ALIGN_PARAGRAPH.RIGHT, 2)
        r = p.add_run(title)
        set_run_font(r, 14)
        p.add_run("\t")
        page = toc_pages.get(title, "")
        r2 = p.add_run(str(page))
        set_run_font(r2, 14)


